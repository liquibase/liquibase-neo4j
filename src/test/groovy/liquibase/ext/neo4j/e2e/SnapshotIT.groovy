        uniqueConstraint["labels"] == ["Foo"]
        uniqueConstraint["properties"] == ["description"]
    }

    private Object parseSnapshotReport() {
        new Yaml().load(buffer.toString(StandardCharsets.UTF_8))
    }

    void createNodeIndicesExcept(String excludedType) {
        createNodeIndexStatements.forEach { indexType, statement -> {
            if (indexType != excludedType) {
                queryRunner.run(statement)
            }
        }}
    }

    void dropNodeIndicesExcept(String excludedType) {
        dropNodeIndexStatements.forEach { indexType, statement -> {
            if (indexType != excludedType) {
                queryRunner.run(statement)
            }
        }}
    }

    void createNodeConstraints() {
        createNodeConstraintsExcept(null)
    }

    void dropNodeConstraints() {
        dropNodeConstraintsExcept(null)
    }

    void createNodeConstraintsExcept(String excludedType) {
        createNodeConstraintStatements.forEach { indexType, statement -> {
            if (excludedType == null || indexType != excludedType) {
                queryRunner.run(statement)
            }
        }}
    }

    void dropNodeConstraintsExcept(String excludedType) {
        dropNodeConstraintStatements.forEach { indexType, statement -> {
            if (excludedType == null || indexType != excludedType) {
                queryRunner.run(statement)
            }
        }}
    }

    private insertData() {
        queryRunner.run("CREATE (f:Foo {" +
                "bar: 'qix', " +
                "baz: 42, " +
                "location: point({latitude: toFloat('37.89'), longitude: toFloat('41.12')}), " +
                "description: 'lorem ipsum', " +
                "embedding: " + cypherEmbeddings() +
                "})-[:FIGHTERS]->()")
    }

